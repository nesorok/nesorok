clear all
clc

x1 = [0 1 0 0];
y1 = [0 0 1 0];
z1 = [0 0 0 1];

coord = zeros(4);
coord(:, 1) = 1;
coord(:, 2) = x1;
coord(:, 3) = y1;
coord(:, 4) = z1;

E = eye(4);
coef = coord \ E;

L = [1; x1; y1; z1] * coef;

for i = 1:4
    N1 = L(1, i);
    
    C1 = [N1*x1(2) y1(2) z1(2) 0;
          N1*x1(3) y1(3) z1(3) 0;
          N1*x1(4) y1(4) z1(4) 0];

    C2 = [N1*x1(1) y1(1) z1(1) 0;
          N1*x1(2) y1(2) z1(2) 0;
          N1*x1(4) y1(4) z1(4) 0];

    C3 = [N1*x1(1) y1(1) z1(1) 0;
          N1*x1(3) y1(3) z1(3) 0;
          N1*x1(4) y1(4) z1(4) 0];

    C4 = [N1*x1(1) y1(1) z1(1) 0;
          N1*x1(2) y1(2) z1(2) 0;
          N1*x1(3) y1(3) z1(3) 0];

    figure
    hold on
    grid on
    fill3([x1(2), 1; x1(3), 1; x1(4), 1], [y1(2), 1; y1(3), 1; y1(4), 1], [z1(2), 1; z1(3), 1; z1(4), 1], C1)
    fill3([x1(1), 1; x1(2), 1; x1(4), 1], [y1(1), 1; y1(2), 1; y1(4), 1], [z1(1), 1; z1(2), 1; z1(4), 1], C2)
    fill3([x1(1), 1; x1(3), 1; x1(4), 1], [y1(1), 1; y1(3), 1; y1(4), 1], [z1(1), 1; z1(3), 1; z1(4), 1], C3)
    fill3([x1(1), 1; x1(2), 1; x1(3), 1], [y1(1), 1; y1(2), 1; y1(3), 1], [z1(1), 1; z1(2), 1; z1(3), 1], C4)
    xlabel('Îñü X')
    ylabel('Îñü Y')
    zlabel('Îñü Z')
end